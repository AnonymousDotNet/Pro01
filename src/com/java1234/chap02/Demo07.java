package com.java1234.chap02;

//运算符与表达式
public class Demo07 {
	public static void main(String[] args) {
		int a = 10;
		int b = 3;
		// +运算符
		System.out.println(a + "+" + b + "=" + (a + b));
		// -运算符
		System.out.println(a + "-" + b + "=" + (a - b));
		// *运算符
		System.out.println(a + "*" + b + "=" + (a * b));
		// /运算符
		System.out.println(a + "/" + b + "=" + (a / b));
		// %（取余）运算符
		System.out.println(a + "%" + b + "=" + (a % b));
	}
}
